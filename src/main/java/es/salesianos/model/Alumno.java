package es.salesianos.model;

public class Alumno{
	private String nombre;
	private int id;
	private String fct;
	
	
	public String getFct() {
		return fct;
	}

	public void setFct(String fct) {
		this.fct = fct;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}




}
